package org.ftcteam5206.utils.vectors;

// 2D vectors
public class vector2d {
    public double x;
    public double y;

    public vector2d(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public vector2d(){}

    public double getX() { return x; }
    public double getY() { return y; }
}
